package com.my.diseaseControl.vo;

public class GIS {
	
	private int GISID = 0;
	private String GISImageUrl = null;
	private String SHPFileUrl = null;
	
	
	
	public int getGISID() {
		return GISID;
	}
	public void setGISID(int gISID) {
		GISID = gISID;
	}
	public String getGISImageUrl() {
		return GISImageUrl;
	}
	public void setGISImageUrl(String gISImageUrl) {
		GISImageUrl = gISImageUrl;
	}
	public String getSHPFileUrl() {
		return SHPFileUrl;
	}
	public void setSHPFileUrl(String sHPFileUrl) {
		SHPFileUrl = sHPFileUrl;
	}
	
	
}
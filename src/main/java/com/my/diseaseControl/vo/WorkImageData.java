package com.my.diseaseControl.vo;


public class WorkImageData {
	private int ImageDataIndex = 0;
	private int ProjectID = 0;
	private String ImageUrl = null;
	private String ImageType = null;
	
	
	
	public int getImageDataIndex() {
		return ImageDataIndex;
	}
	public void setImageDataIndex(int imageDataIndex) {
		ImageDataIndex = imageDataIndex;
	}
	public int getProjectID() {
		return ProjectID;
	}
	public void setProjectID(int projectID) {
		ProjectID = projectID;
	}
	public String getImageUrl() {
		return ImageUrl;
	}
	public void setImageUrl(String imageUrl) {
		ImageUrl = imageUrl;
	}
	public String getImageType() {
		return ImageType;
	}
	public void setImageType(String imageType) {
		ImageType = imageType;
	}
	

	
}